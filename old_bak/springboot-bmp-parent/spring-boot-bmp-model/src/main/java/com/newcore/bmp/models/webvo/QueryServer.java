package com.newcore.bmp.models.webvo;

public class QueryServer {
	String id; 
	String systemEnglishName; 
	String provCode; 
	String serverName; 
	String ip; 
	String port; 
	String userName; 
	String charset; 
	String system;
	
	String order; 
	String length;
	String start;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getSystemEnglishName() {
		return systemEnglishName;
	}
	public void setSystemEnglishName(String systemEnglishName) {
		this.systemEnglishName = systemEnglishName;
	}
	public String getProvCode() {
		return provCode;
	}
	public void setProvCode(String provCode) {
		this.provCode = provCode;
	}
	public String getServerName() {
		return serverName;
	}
	public void setServerName(String serverName) {
		this.serverName = serverName;
	}
	public String getIp() {
		return ip;
	}
	public void setIp(String ip) {
		this.ip = ip;
	}
	public String getPort() {
		return port;
	}
	public void setPort(String port) {
		this.port = port;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getCharset() {
		return charset;
	}
	public void setCharset(String charset) {
		this.charset = charset;
	}
	public String getSystem() {
		return system;
	}
	public void setSystem(String system) {
		this.system = system;
	}
	public String getOrder() {
		return order;
	}
	public void setOrder(String order) {
		this.order = order;
	}
	public String getLength() {
		return length;
	}
	public void setLength(String length) {
		this.length = length;
	}
	public String getStart() {
		return start;
	}
	public void setStart(String start) {
		this.start = start;
	} 
	
	
}
