package com.newcore.bmp.models.authority.models;

import java.io.Serializable;
import java.util.List;

/**
 * @author lhq 操作员信息Model.
 */
public class Clerk implements Serializable {

    private static final long serialVersionUID = 77713697475591066L;
    /* 机构代码 */
    private String branchNo;
    /* 部门号 */
    private Integer deptNo;
    /* 操作员工号 */
    private String userName;
    /* 姓名 */
    private String name;
    /* 密码 */
    private String password;
    /* 加密密码的盐 */
    private String passwordSalt;
    /* 固定电话 */
    private String tel;
    /* 手机号 */
    private String mobile;
    /* 电子邮箱 */
    private String email;
    /* 是否锁定（N，Y） */
    private String lockFlag;
    /* 是否锁定（N，Y） 描述 */
    private String lockFlagDesc;
    /* 是否接收任务（N，Y） */
    private String acceptTaskFlag;
    /* 是否接收任务（N，Y） 描述*/
    private String acceptTaskFlagDesc;
    /* 操作员状态 */
    private String clerkStatus;
    /* 操作员状态  描述 */
    private String clerkStatusDesc;
    /* 核算单元 */
    private String centerCode;
    /* 连续登录失败次数 */
    private Integer loginFailNum;
    
    private List<String> roleIds;

    public String getCredentialsSalt() {
        return this.userName + this.passwordSalt;
    }

    /**
     * @return the branchNo
     */
    public String getBranchNo() {
        return branchNo;
    }

    /**
     * @param branchNo
     *            the branchNo to set
     */
    public void setBranchNo(String branchNo) {
        this.branchNo = branchNo;
    }
  
    /**
     * @return the deptNo
     */
    public Integer getDeptNo() {
        return deptNo;
    }

    /**
     * @param deptNo the deptNo to set
     */
    public void setDeptNo(Integer deptNo) {
        this.deptNo = deptNo;
    }

    /**
     * @return the clerkNo
     */
    public String getUserName() {
        return userName;
    }

    /**
     * @param clerkNo
     *            the clerkNo to set
     */
    public void setUserName(String clerkNo) {
        this.userName = clerkNo;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name
     *            the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the password
     */
    public String getPassword() {
        return password;
    }

    /**
     * @param password
     *            the password to set
     */
    public void setPassword(String password) {
        this.password = password;
    }

    /**
     * @return the tel
     */
    public String getTel() {
        return tel;
    }

    /**
     * @param tel
     *            the tel to set
     */
    public void setTel(String tel) {
        this.tel = tel;
    }

    /**
     * @return the mobile
     */
    public String getMobile() {
        return mobile;
    }

    /**
     * @param mobile
     *            the mobile to set
     */
    public void setMobile(String mobile) {
        this.mobile = mobile;
    }

    /**
     * @return the email
     */
    public String getEmail() {
        return email;
    }

    /**
     * @param email
     *            the email to set
     */
    public void setEmail(String email) {
        this.email = email;
    }
   
    /**
     * @return the centerCode
     */
    public String getCenterCode() {
        return centerCode;
    }

    /**
     * @param centerCode
     *            the centerCode to set
     */
    public void setCenterCode(String centerCode) {
        this.centerCode = centerCode;
    }

    /**
     * @return the passwordSalt
     */
    public String getPasswordSalt() {
        return passwordSalt;
    }

    /**
     * @param passwordSalt
     *            the passwordSalt to set
     */
    public void setPasswordSalt(String passwordSalt) {
        this.passwordSalt = passwordSalt;
    }

    /**
     * @return the lockFlag
     */
    public String getLockFlag() {
        return lockFlag;
    }

    /**
     * @param lockFlag the lockFlag to set
     */
    public void setLockFlag(String lockFlag) {
        this.lockFlag = lockFlag;
    }

    /**
     * @return the lockFlagDesc
     */
    public String getLockFlagDesc() {
        return lockFlagDesc;
    }

    /**
     * @param lockFlagDesc the lockFlagDesc to set
     */
    public void setLockFlagDesc(String lockFlagDesc) {
        this.lockFlagDesc = lockFlagDesc;
    }

    /**
     * @return the acceptTaskFlag
     */
    public String getAcceptTaskFlag() {
        return acceptTaskFlag;
    }

    /**
     * @param acceptTaskFlag the acceptTaskFlag to set
     */
    public void setAcceptTaskFlag(String acceptTaskFlag) {
        this.acceptTaskFlag = acceptTaskFlag;
    }

    /**
     * @return the clerkStatus
     */
    public String getClerkStatus() {
        return clerkStatus;
    }

    /**
     * @param clerkStatus the clerkStatus to set
     */
    public void setClerkStatus(String clerkStatus) {
        this.clerkStatus = clerkStatus;
    }

    /**
     * @return the acceptTaskFlagDesc
     */
    public String getAcceptTaskFlagDesc() {
        return acceptTaskFlagDesc;
    }

    /**
     * @param acceptTaskFlagDesc the acceptTaskFlagDesc to set
     */
    public void setAcceptTaskFlagDesc(String acceptTaskFlagDesc) {
        this.acceptTaskFlagDesc = acceptTaskFlagDesc;
    }

    /**
     * @return the clerkStatusDesc
     */
    public String getClerkStatusDesc() {
        return clerkStatusDesc;
    }

    /**
     * @param clerkStatusDesc the clerkStatusDesc to set
     */
    public void setClerkStatusDesc(String clerkStatusDesc) {
        this.clerkStatusDesc = clerkStatusDesc;
    }

    /**
     * @return the loginFailNum
     */
    public Integer getLoginFailNum() {
        return loginFailNum;
    }

    /**
     * @param loginFailNum the loginFailNum to set
     */
    public void setLoginFailNum(Integer loginFailNum) {
        this.loginFailNum = loginFailNum;
    }

    /**
     * @return the roleIds
     */
    public List<String> getRoleIds() {
        return roleIds;
    }

    /**
     * @param roleIds the roleIds to set
     */
    public void setRoleIds(List<String> roleIds) {
        this.roleIds = roleIds;
    }
    
}